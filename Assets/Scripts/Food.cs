namespace TamaCovid
{
    /// <summary>
    /// Class containing data regarding
    /// a food item.
    /// </summary>
    public class Food
    {
        /// <summary>
        /// Name of the food item.
        /// </summary>
        public string name;

        /// <summary>
        /// Description of the food item.
        /// </summary>
        public string description;

        /// <summary>
        /// Amount of hunger restored when consuming
        /// this food item.
        /// </summary>
        public int hungerRestoreAmount;
    }
}
